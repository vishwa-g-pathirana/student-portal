    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        
        if(mdCode.getText().trim().isEmpty() && stID.getText().trim().isEmpty())
        {
            displayAlert.setText("Module code and Student id is empty!");
        }
        
        else if(mdCode.getText().trim().isEmpty())
        {
            displayAlert.setText("Module code cannot be empty");
        }
        
        else if(stID.getText().trim().isEmpty())
        {
            displayAlert.setText("Student-Id cannot be empty!");
        }
        else
        {
            displayAlert.setText("");
        }
       
       
        try {
            
            String myModule = mdCode.getText();
            String myId = stID.getText();
            
            pst = (PreparedStatement) con.prepareStatement("INSERT INTO attendance(modulecode,studentid) VALUES(?,?)");
            pst.setString(1, myModule);
            pst.setString(2, myId);
            
             int updt = pst.executeUpdate();
            
            if(updt == 1)
            {
                JOptionPane.showMessageDialog(this,"Attendance marked.");
                mdCode.setText("");
                stID.setText("");
                mdCode.requestFocus();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Failed to mark attendance.");
            }
            
        } 
        catch (SQLException ex) {
            Logger.getLogger(attendence.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_submitButtonActionPerformed

