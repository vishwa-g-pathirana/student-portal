    void LoadAttendance()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/2checkdb", "root", "");
         
            pst = (PreparedStatement) con.prepareStatement("SELECT time FROM attendance WHERE modulecode=? AND studentid=?");
            pst.setString(1, mdCode1.getText());
            pst.setString(2, stID1.getText());
            ResultSet rs = pst.executeQuery();
            while(atDateandTime.getRowCount()>0)
            {
                ((DefaultTableModel)atDateandTime.getModel()).removeRow(0);
            }
            
            int col = rs.getMetaData().getColumnCount();
            while(rs.next())
            {
              Object [] rows = new Object[col];
              for(int i=1;i<=col;i++)
              {
                  rows[i-1] = rs.getObject(i);
              }
              ((DefaultTableModel)atDateandTime.getModel()).insertRow(rs.getRow() -1, rows);
            
            }
            rs.close();
            pst.close();
           
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CrudForAdminOnG.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CrudForAdminOnG.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        
        
    }
    
    
    
    
    
    
    
