    void LoadLectures()
    {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/2checkdb", "root", "");
            Statement st = con.createStatement();
            ResultSet rst = st.executeQuery("SELECT * FROM lecsupc");
           
            
            while(jTable2.getRowCount()>0)
            {
                ((DefaultTableModel)jTable2.getModel()).removeRow(0);
            }
            
            int col = rst.getMetaData().getColumnCount();
            while(rst.next())
            {
              Object [] rows = new Object[col];
              for(int i=1;i<=col;i++)
              {
                  rows[i-1] = rst.getObject(i);
              }
              ((DefaultTableModel)jTable2.getModel()).insertRow(rst.getRow() -1, rows);
            
            }
            rst.close();
            st.close();
           
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CrudForAdminOnG.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CrudForAdminOnG.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        
        
    }
    
