    private void addToTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToTableActionPerformed
        try {
            // TODO add your handling code here:
            String modCode = mdCode.getText();
            String lTime = lecTime.getText();
            String ltrr = lecturer.getText();
            String lecture = lec.getText();

            pst = con.prepareStatement("INSERT INTO lecsupc(modulecode,time,lecturer,module) VALUES(?,?,?,?)");
            pst.setString(1, modCode);
            pst.setString(2, lTime);
            pst.setString(3, ltrr);
            pst.setString(4, lecture);

            int updt = pst.executeUpdate();

            if(updt == 1)
            {
                JOptionPane.showMessageDialog(this,"Lecture added");
                mdCode.setText("");
                lecTime.setText("");
                lecturer.setText("");
                lec.setText("");
                mdCode.requestFocus();
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Failed to add lecture");
            }

        }

        catch (SQLException ex) {
            Logger.getLogger(CrudForAdminOnG.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addToTableActionPerformed

